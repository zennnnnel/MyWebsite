    <h1>Welcome to Laravel</h1>
    <p><b>What is Laravel?</b></p>
    <p>Laravel is a free, open-source PHP framework used to build modern web applications easily. It follows the MVC (Model-View-Controller) architecture, providing clean and structured code.</p>

    <h2>How to Set Up Laravel</h2>
    <ol>
        <li>Install Composer from <a href="https://getcomposer.org/">getcomposer.org</a>.</li>
        <li>Run <code>composer create-project laravel/laravel projectname</code>.</li>
        <li>Go to your project folder using <code>cd projectname</code>.</li>
        <li>Run <code>php artisan serve</code> to start the local server.</li>
        <li>Open your browser and visit <code>http://127.0.0.1:8000</code>.</li>
    </ol>

